package Stringss;

public class PrettyPrinting {
    public static void main(String[] args) {
        float a = 456.23212f;
        System.out.printf("Formated number is %.2f ",a);

        System.out.printf("Pie: %.3f", Math.PI);
        System.out.printf("I am %s and i am %s", "Shibli", "Alig");
    }
}
